package tsu.pro.bean;

public class LivingArea {
   private int id;
    private String      livingAreaName;
    private String    livingAreades;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLivingAreaName() {
        return livingAreaName;
    }

    public void setLivingAreaName(String livingAreaName) {
        this.livingAreaName = livingAreaName;
    }

    public String getLivingAreades() {
        return livingAreades;
    }

    public void setLivingAreades(String livingAreades) {
        this.livingAreades = livingAreades;
    }



}

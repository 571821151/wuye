package tsu.pro.bean;

public class Repair {
	 private String repariID;
	 private String repairTime;
	 private String repairInfo ;
	 private int ownerID ;
	 private String repairDel ;
	 private String repairIdentify;
	 private String update_Tm;
	 
	public String getUpdate_Tm() {
		return update_Tm;
	}
	public void setUpdate_Tm(String update_Tm) {
		this.update_Tm = update_Tm;
	}
	public String getRepariID() {
		return repariID;
	}
	public void setRepariID(String repariID) {
		this.repariID = repariID;
	}
	public String getRepairTime() {
		return repairTime;
	}
	public void setRepairTime(String repairTime) {
		this.repairTime = repairTime;
	}
	public String getRepairInfo() {
		return repairInfo;
	}
	public void setRepairInfo(String repairInfo) {
		this.repairInfo = repairInfo;
	}
	public int getOwnerID() {
		return ownerID;
	}
	public void setOwnerID(int ownerID) {
		this.ownerID = ownerID;
	}
	public String getRepairDel() {
		return repairDel;
	}
	public void setRepairDel(String repairDel) {
		this.repairDel = repairDel;
	}
	public String getRepairIdentify() {
		return repairIdentify;
	}
	public void setRepairIdentify(String repairIdentify) {
		this.repairIdentify = repairIdentify;
	} 
	 
}
